package marafon_java.day01;

/**
 * Реализация 6 задания с циклом while.
 */
public class Task6_2 {
    public static void main(String[] args) {
        int k = 5;
        int i = 1;
        while (i <= 9) {
            System.out.println(i + " x " + k + " = " + i*k);
            i = i + 1;
        }
    }
}
